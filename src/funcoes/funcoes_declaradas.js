// funcoes-declaradas.js

// === FUNÇÕES DECLARADAS ===
// Funções criadas com a palavra-chave `function`.

console.log('=== FUNÇÕES DECLARADAS ===');

// Exemplo básico de uma função declarada
function saudacao() {
    console.log('Olá! Seja bem-vindo ao tutorial de funções.');
}

// Chamando a função
saudacao();

// Função com parâmetros
function soma(a, b) {
    console.log(`A soma de ${a} e ${b} é: ${a + b}`);
}

// Chamando a função com argumentos
soma(3, 5); // Saída: A soma de 3 e 5 é: 8
