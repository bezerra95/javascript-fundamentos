// other-types.js

// Outros tipos de dados incluem objetos, arrays e funções.
// Esses serão abordados mais detalhadamente em tópicos futuros.

// Exemplo de um objeto
const pessoa = {
    nome: 'João',
    idade: 30,
};

// Exemplo de um array
const numeros = [1, 2, 3, 4, 5];

// Exemplo de uma função
function saudacao(nome) {
    return `Olá, ${nome}!`;
}

// Exibindo os outros tipos no console
console.log('Objeto:', pessoa);
console.log('Array:', numeros);
console.log('Função chamada:', saudacao('Maria'));
