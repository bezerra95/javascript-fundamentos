// number.js

// TIPO NUMBER
// O tipo Number é usado para representar números inteiros e decimais.

// Exemplo de números inteiros e decimais
const inteiro = 42;
const decimal = 3.14;

// Exibindo os números no console
console.log('Número inteiro:', inteiro);
console.log('Número decimal:', decimal);

// Operações matemáticas básicas
const soma = inteiro + decimal; // Adição
const subtracao = inteiro - decimal; // Subtração
const multiplicacao = inteiro * decimal; // Multiplicação
const divisao = inteiro / decimal; // Divisão

console.log('Soma:', soma);
console.log('Subtração:', subtracao);
console.log('Multiplicação:', multiplicacao);
console.log('Divisão:', divisao);
