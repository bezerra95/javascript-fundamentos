// boolean.js

// TIPO BOOLEAN
// Um boolean representa um valor lógico: true ou false.

// Exemplos de valores booleanos
const verdadeiro = true;
const falso = false;

// Exibindo os valores booleanos no console
console.log('Valor verdadeiro:', verdadeiro);
console.log('Valor falso:', falso);

// Usando booleanos em expressões
const maiorQue = 10 > 5; // true
const menorQue = 10 < 5; // false

console.log('10 é maior que 5?', maiorQue);
console.log('10 é menor que 5?', menorQue);
